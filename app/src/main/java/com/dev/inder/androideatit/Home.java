    private void loadMenu() {
        adapter = new FirebaseRecyclerAdapter<com.dev.inder.androideatit.model.Category, MenuViewHolder>(Category.class,R.layout.menu_item,MenuViewHolder.class,category) {
            @Override
            protected void populateViewHolder(MenuViewHolder viewHolder, Category model, int position) {
                viewHolder.textViewname.setText(model.getName());
                Glide.with(getBaseContext()).load(model.getImage()).into(viewHolder.imageView);
                final Category clickitem = model;
                viewHolder.setItemClickListerner(new ItemClickListerner() {
                    @Override
                    public void Onclick(View view, int position, boolean isLongClick) {
                        Toast.makeText(Home.this, adapter.getRef(position).getKey(), Toast.LENGTH_SHORT).show();
                        Intent foodList = new Intent(getApplicationContext(),FoodList.class);
                        foodList.putExtra("CategoryId",adapter.getRef(position).getKey());
                        startActivity(foodList);
                    }
                });
            }
        };
        recyclerView.setAdapter(adapter);
    }

    @Override
    public void onBackPressed() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            super.onBackPressed();
        }
    }

